// Character mapping: Space, 0-9, A-Z, special chars
// Index calculation: Space=0, 0-9=1-10, A-Z=11-36, special chars after

int get_char_index(char c) {
    if (c == ' ') return 0;
    if (c >= '0' && c <= '9') return 1 + (c - '0');
    if (c >= 'A' && c <= 'Z') return 11 + (c - 'A');
    if (c >= 'a' && c <= 'z') return 11 + (c - 'a'); // lowercase = uppercase
    if (c == '%') return 37;
    if (c == '.') return 38;
    if (c == ':') return 39;
    if (c == '/') return 40;
    if (c == '-') return 41;
    if (c == '+') return 42;
    if (c == '°') return 43; // Degree symbol
    return 0; // Unknown = space
}

